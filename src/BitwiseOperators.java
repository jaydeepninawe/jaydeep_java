public class BitwiseOperators {
    public static void main(String[] args) {
        int a = 5, b = 6;

        int c = a & b;
        int d = a | b;
        int e = a ^ b;
        int f = ~a;

        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
