public class arithmetic {

        public static void main(String[] args) {
            int a = 7, b = 4;

            // + for addition
            int c = a + b;
            // - for subtraction
            int d = a - b;
            // / for division
            int e = a / b;
            // * for multiplication
            int f = a * b;
            // % for remainder
            int g = a % b;

            System.out.println(c); // 11
            System.out.println(d); // 3
            System.out.println(e); // 1
            System.out.println(f); // 28
            System.out.println(g); // 3
        }
    }


